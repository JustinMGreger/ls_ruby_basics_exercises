Description of the exercise:
Using the ruby documentation, determine how you would write a Symbol that represents your name. 
We aren't looking for a String; we want a Symbol, which is one of ruby's datatypes.

Description of the solution:
Go to https://docs.ruby-lang.org/en/ or https://ruby-doc.org/
Select the API for the version of Ruby you are using.
Search for "literals".
Once in the "literals" section search for "symbol".

Answer:
The documentation says " You may reference a symbol using a colon: :my_symbol"
To write a symbol that represents your name you would write:
:Name
