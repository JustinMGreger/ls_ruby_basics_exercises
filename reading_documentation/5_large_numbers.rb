Description of exercise:
Using the ruby documentation, determine how you can write large numbers in a way that makes them easier to read.

Description of solution:
Go to https://docs.ruby-lang.org/en/ or https://ruby-doc.org/
Select the API for the version of Ruby you are using.
Search for "literals".
Once in the "literals" section search for "number".
This sould bring you to Number Literals & specifically Integer Literals.
