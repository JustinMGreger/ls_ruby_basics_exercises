Exercise:
Using the ruby documentation, determine what value a while loop returns.

Answer:
Go to https://docs.ruby-lang.org/en/ or https://ruby-doc.org/
Select the API for the version of Ruby you are using.
Search for "control expressions".
In the "control expressions" section search for "while loop".
Read the "while loop" section for the answer.
The return value of a while loop is nil unless break is used to supply a value.
